    /**
     * Sets the player buttons, such as mute and play / pause button
     * @see audio.SoundJLayer The mp3 player
     * @see #btnPlay play / pause
     * @see SoundJLayer#setGain(float) set volume + mute
     */
    public void setPlayerControls(){
        
        // add icons for interchangeablity (play / pause)
        play = new ImageIcon("src\\GUI\\play.png");
        pause = new ImageIcon("src\\GUI\\pause.png");
        
        sliderVolume.setValue(SoundJLayer.volumeLevel);
        
        if(SoundJLayer.isPaused == false){
            btnPlay.setIcon(pause);
        }
        else{
             btnPlay.setIcon(play);
        }
        
        if(SoundJLayer.isMuted == true){
            musicCheck.setSelected(true);
        }
        else{
            musicCheck.setSelected(false);
        }
    }
