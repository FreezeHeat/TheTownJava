    /**
     * <i>Action</i> when clicked <b>button</b> mutes / un-mutes the game
     * @param evt the event itself
     */
    private void musicCheckActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_musicCheckActionPerformed
        // mute or unmute the sound
        if(musicCheck.isSelected()){
            SoundJLayer.isMuted = true;
            SoundJLayer.setGain(0);
        }
        else{
            SoundJLayer.isMuted = false;
            SoundJLayer.volumeLevel = sliderVolume.getValue();
            SoundJLayer.setGain(sliderVolume.getValue() / 100f);
        }
    }//GEN-LAST:event_musicCheckActionPerformed

    /**
     * <i>Action</i> when slider is moved, handles volume gain
     * @see audio.SoundJLayer#setGain(float) set volume function
     * @param evt the event itself
     */
    private void sliderVolumeStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_sliderVolumeStateChanged
        //change volume according to slider
        SoundJLayer.volumeLevel = sliderVolume.getValue();
        
        if(SoundJLayer.isMuted == false){
            SoundJLayer.setGain(sliderVolume.getValue() / 100f);
        }
    }//GEN-LAST:event_sliderVolumeStateChanged

     /**
     * <i>Action</i> when clicked <b>button</b> play / pauses music
     * @param evt the event itself
     */
    private void btnPlayActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPlayActionPerformed
        
        /* play / pause music */
        
        // Player is working
        if(SoundJLayer.isPaused == false){

            //set icons
            btnPlay.setIcon(play);

            //inform the player to pause
            Main.player.pause();
        }

        // Player isn't working
        else{

            //set icons
            btnPlay.setIcon(pause);

            //inform thread to resume
             Main.player.play();
        }
    }//GEN-LAST:event_btnPlayActionPerformed

